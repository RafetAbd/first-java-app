package pkg1;

public class Spare {

    public int multiply(int num1,int num2) {
        return num1 * num2;
    }

    public double multiply(double num1,double num2) {
        return num1 * num2;
    }

    public double multiply(double num1,double num2, int num3) {
        return num1 * num2 + num3;
    }

}
